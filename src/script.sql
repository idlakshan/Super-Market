DROP DATABASE IF EXISTS supermarket;
CREATE DATABASE IF NOT EXISTS supermarket;
SHOW DATABASES;

USE supermarket;

DROP TABLE IF EXISTS customerDTO;
CREATE TABLE IF NOT EXISTS customerDTO(
    id VARCHAR(15),
    name VARCHAR(45) NOT NULL DEFAULT 'Unknown',
    address TEXT,
    tp VARCHAR(12),
    CONSTRAINT PRIMARY KEY (id)
    );
SHOW TABLES ;
DESCRIBE customerDTO;
#---------------------
DROP TABLE IF EXISTS `orderDTO`;
CREATE TABLE IF NOT EXISTS `orderDTO`(
    oId VARCHAR(15),
    cId VARCHAR(15),
    date DATE,
    time VARCHAR(15),
    cost DOUBLE,
    CONSTRAINT PRIMARY KEY (oId),
    CONSTRAINT FOREIGN KEY (cId) REFERENCES customerDTO(id) ON DELETE CASCADE ON UPDATE CASCADE
    );
SHOW TABLES;
DESCRIBE `orderDTO`;
#-----------------------
DROP TABLE IF EXISTS itemDTO;
CREATE TABLE IF NOT EXISTS itemDTO(
    code VARCHAR(15),
    name TEXT,
    qtyOnHand INT DEFAULT 0,
    price DOUBLE DEFAULT 0.00,
    CONSTRAINT PRIMARY KEY (code)
    );
SHOW TABLES ;
DESCRIBE itemDTO;
#------------------------
DROP TABLE IF EXISTS `orderDTO detail`;
CREATE TABLE IF NOT EXISTS `orderDTO detail`(
    itemCode VARCHAR(15),
    orderId VARCHAR(15),
    qty INT,
    price DOUBLE,
    CONSTRAINT PRIMARY KEY (itemCode, orderId),
    CONSTRAINT FOREIGN KEY (itemCode) REFERENCES itemDTO(code) ON DELETE CASCADE ON UPDATE CASCADE ,
    CONSTRAINT FOREIGN KEY (orderId) REFERENCES `orderDTO`(oId) ON DELETE CASCADE ON UPDATE CASCADE
    );
SHOW TABLES ;
DESCRIBE `orderDTO detail`;
